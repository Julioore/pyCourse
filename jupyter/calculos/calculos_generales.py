def sumar(n1,n2):
    print(f"El resultado de la suma es {n1+n2}")

def restar(n1,n2):
    print(f"El resultado de la resta es {n1-n2}")

def multiplicar(n1,n2):
    print(f"El resultado del producto es {n1*n2}")

def dividir(n1,n2):
    print(f"El resultado de dividir {n1} entre {n2} es {n1/n2}")

def potencia(n1,n2):
    print(f"El resultado de elevar {n1} a {n2} es {n1**n2}")

def redondear(n1,n2):
    print(f"El resultado de redondear {n1} a {n2} decimales es {round(n1,n2)}")